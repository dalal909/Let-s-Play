        ),
        titleLarge: TextStyle(
          color: appTheme.whiteA700,
          fontSize: 20.fSize,
          fontFamily: 'Athiti',
          fontWeight: FontWeight.w400,
        ),
      );
}

/// Class containing the supported color schemes.
class ColorSchemes {
  static final primaryColorScheme = ColorScheme.light();
}

/// Class containing custom colors for a primary theme.
class PrimaryColors {
  // Black
  Color get black900 => Color(0XFF010104);
  Color get black90001 => Color(0XFF030303);
  Color get black90002 => Color(0XFF08090A);
  Color get black90003 => Color(0XFF000000);
// BlueGray
  Color get blueGray400 => Color(0XFF888888);
// Gray
  Color get gray50 => Color(0XFFF7FAFC);
  Color get gray800 => Color(0XFF494949);
// Indigo
  Color get indigo5044 => Color(0X44E9ECF9);
// White
  Color get whiteA700 => Color(0XFFFFFFFF);
}
